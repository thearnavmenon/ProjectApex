CREATE OR REPLACE FUNCTION "public"."match_memory_embeddings"("query_embedding" "public"."vector", "p_user_id" "uuid", "match_threshold" double precision DEFAULT 0.75, "match_count" integer DEFAULT 3) RETURNS TABLE("id" "uuid", "raw_transcript" "text", "tags" "text"[], "metadata" "jsonb", "created_at" timestamp with time zone, "similarity" double precision)
    LANGUAGE "sql" STABLE
    AS $$
  SELECT me.id, me.raw_transcript, me.tags, me.metadata, me.created_at,
    1 - (me.embedding <=> query_embedding) AS similarity
  FROM public.memory_embeddings me
  WHERE me.user_id = p_user_id
    AND 1 - (me.embedding <=> query_embedding) > match_threshold
  ORDER BY me.embedding <=> query_embedding
  LIMIT match_count;
$$;


